#Write a program to determine the average of 5 test scores
#test 1 through test 5 will be the variables representing the test scores
#average represents the average test score out of the five

test1=float(input("Please enter the first test score:"))
test2=float(input("Please enter the second test score:"))
test3=float(input("Please enter the third test score:"))
test4=float(input("Please enter the forth test score:"))
test5=float(input("Please enter the fifth test score:"))
average=(test1+test2+test3+test4+test5)/5
print("The average test score is", average)
